//% Implements Fortran-callable wrappers around the Linux resource limit functions.

#include <sys/time.h>
#include <sys/resource.h>

int rLimitCPU = RLIMIT_CPU;

int setResourceLimit_C(int resource, long limit) {
  //% Fortran-callable wrapper around the setrlimit() function to set a resource limit.
  struct rlimit rlim;
  rlim.rlim_cur = limit;
  rlim.rlim_max = RLIM_INFINITY;
  int status = setrlimit(resource,&rlim);
  return status;
}
