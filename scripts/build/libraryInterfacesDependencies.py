#!/usr/bin/env python3
"""Build a Makefile fragment with per-class .F90 -> .o rules for libgalacticus.

Lists the .F90 files actually generated by libraryInterfaces.py in
``$BUILDPATH/libgalacticus/`` and emits compile rules for each.  This
covers both the per-class ``<class>.F90`` (containing GetPtr,
GetIdAndPtr, methods, destructor) and the per-impl
``<class>__<impl>.F90`` files holding individual constructor wrappers
— the latter exist so gfortran doesn't have to swallow every impl in a
single compilation unit (originally OOM-killed on galacticFilter and
nodePropertyExtractor).

Andrew Benson (ported to Python 2026; per-impl split 2026)
"""
import os
import sys

build_path = os.environ['BUILDPATH'].rstrip('/') + '/'
out_dir    = os.path.join(build_path, 'libgalacticus')

if not os.path.isdir(out_dir):
    # libraryInterfaces.py hasn't run yet (fresh checkout).  The
    # Makefile makes Makefile_Library_Dependencies prereq-on
    # libgalacticus.Inc, so this script gets re-run automatically once
    # libraryInterfaces.py has populated out_dir.
    sys.exit(
        f"libraryInterfacesDependencies.py: {out_dir} not found — "
        f"libraryInterfaces.py must run first to populate the per-class "
        f".F90 files this rule generator enumerates."
    )

# Each .F90 file in out_dir is one compilation unit; treat them all
# uniformly.  Sorted for deterministic output (helps reproducible builds
# and avoids spurious git diffs in the generated Makefile fragment).
units = sorted(f[:-len('.F90')]
               for f in os.listdir(out_dir)
               # Exclude preprocessed `<unit>.p.F90` files, which live in the
               # same directory: they are build products of the units, not
               # units themselves.
               if f.endswith('.F90') and not f.endswith('.p.F90'))

rules = []
for name in units:
    rules.append(
        f"{build_path}libgalacticus/{name}.F90:\n"
        f"\t./scripts/build/libraryInterfaces.py\n"
        f"{build_path}libgalacticus/{name}.p.F90.up:"
        f" {build_path}libgalacticus/{name}.F90"
        f" {build_path}hdf5FCInterop.dat"
        f" {build_path}openMPCriticalSections.xml"
        f" {build_path}stateStorables.xml"
        f" {build_path}deepCopyActions.xml\n"
        f"\t./scripts/build/preprocess.py"
        f" {build_path}libgalacticus/{name}.F90"
        f" {build_path}libgalacticus/{name}.p.F90\n"
        f"{build_path}libgalacticus/{name}.p.F90 :"
        f" {build_path}libgalacticus/{name}.p.F90.up\n"
        f"\t@true\n"
        # The compile recipe mirrors the main Makefile's `%.o` pattern rule
        # (see the comment there): diagnostics go to a file so the compiler's
        # exit status is preserved rather than being masked by the
        # postprocess.py pipe. Keep the two recipes in sync.
        f"{build_path}libgalacticus/{name}.o :"
        f" {build_path}libgalacticus/{name}.p.F90"
        f" {build_path}libgalacticus/{name}.d Makefile\n"
        f"\t@mkdir -p {build_path}/moduleBuild\n"
        f"\t$(FCCOMPILER) -c {build_path}libgalacticus/{name}.p.F90"
        f" -o {build_path}libgalacticus/{name}.o $(FCFLAGS)"
        f" > {build_path}libgalacticus/{name}.o.diag 2>&1; \\\n"
        f"\tcompileStatus=$$?; \\\n"
        f"\t./scripts/build/postprocess.py {build_path}libgalacticus/{name}.p.F90"
        f" < {build_path}libgalacticus/{name}.o.diag; \\\n"
        f"\tpostprocessStatus=$$?; \\\n"
        f"\trm -f {build_path}libgalacticus/{name}.o.diag; \\\n"
        f"\t[ $$compileStatus -eq 0 ] && [ $$postprocessStatus -eq 0 ]\n"
        f"\n"
    )

# Rule for libgalacticus_classes.d (concatenates every per-unit .d).
# The aggregator is also forced to re-run whenever
# `Makefile_Library_Dependencies` itself is regenerated — because the
# prereq list of `libgalacticus_classes.d` changes when an impl is
# removed (e.g. a constructor arg starts failing the predicate and
# libraryInterfaces.py drops the impl), Make's mtime check otherwise
# misses the deletion and `libgalacticus_classes.d` keeps its stale
# concatenation containing the now-unbuildable `<class>__<impl>.o`.
# That stale entry then survives into the link command, where Make
# tries to resolve the missing target and errors out with "No rule to
# make target ...".  Adding `Makefile_Library_Dependencies` as a prereq
# means a fresh deps file (which it always is whenever this script
# runs) cascades into a fresh aggregation.
dep_d_files = ' '.join(f"{build_path}libgalacticus/{n}.d" for n in units)
rules.append(
    f"{build_path}libgalacticus_classes.d: {dep_d_files}"
    f" {build_path}Makefile_Library_Dependencies\n"
)
rules.append(f"\t@rm -f {build_path}libgalacticus_classes.d~\n")
for name in units:
    rules.append(f"\t@cat {build_path}libgalacticus/{name}.d >> {build_path}libgalacticus_classes.d~\n")
rules.append(
    f"\t@sort -u {build_path}libgalacticus_classes.d~ -o {build_path}libgalacticus_classes.d~\n"
    f"\t@if cmp -s {build_path}libgalacticus_classes.d {build_path}libgalacticus_classes.d~ ; then \\\n"
    f"\t rm {build_path}libgalacticus_classes.d~ ; \\\n"
    f"\telse \\\n"
    f"\t mv {build_path}libgalacticus_classes.d~ {build_path}libgalacticus_classes.d ; \\\n"
    f"\tfi\n"
)

# Rule for libgalacticus.so — depend on every per-unit .o.
dep_o_files = ' '.join(f"{build_path}libgalacticus/{n}.o" for n in units)
rules.append(f"libgalacticus.so: {dep_o_files}\n")

with open(os.path.join(build_path, "Makefile_Library_Dependencies"), 'w') as fh:
    fh.write(''.join(rules))
