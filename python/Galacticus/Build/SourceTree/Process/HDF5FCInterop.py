"""Rewrites `integer(kind=<hdf5 kind>)` declarations to C-interoperable kinds
using the build-time map at $BUILDPATH/hdf5FCInterop.dat, and imports the
required `ISO_C_Binding` symbols alongside.

Andrew Benson (ported to Python 2026)

Mirrors perl/Galacticus/Build/SourceTree/Process/HDF5FCInterop.pm
"""

import re
import os


from Galacticus.Build.SourceTree                    import walk_tree
from Galacticus.Build.SourceTree.Process            import register_process
from Galacticus.Build.SourceTree.Parse.Declarations import build_declarations
from Galacticus.Build.SourceTree.Parse.ModuleUses   import add_uses


def _load_type_map():
    """Read $BUILDPATH/hdf5FCInterop.dat into a {hdf5_kind: c_kind} dict.

    Mirrors the reader at HDF5FCInterop.pm:22-28.  Raises if the file is absent.
    """
    build_path = os.environ.get('BUILDPATH')
    if not build_path:
        raise RuntimeError(
            "process_hdf5_fc_interop: BUILDPATH environment variable is not set")
    path = os.path.join(build_path, 'hdf5FCInterop.dat')
    if not os.path.exists(path):
        raise RuntimeError(
            f"process_hdf5_fc_interop: type map file does not exist: {path}")
    type_map = {}
    with open(path, 'r') as fh:
        for line in fh:
            # Use `re.search`, not `re.match`: the file generated by
            # hdf5FCInterop.exe / hdf5FCInteropC.exe has a leading space on
            # every line (e.g. " herr_t = c_int") which would defeat an
            # anchored match.  Mirrors the Perl `$line =~ m/(\S+)\s*=\s*(\S+)/`.
            m = re.search(r'(\S+)\s*=\s*(\S+)', line)
            if m:
                type_map[m.group(1)] = m.group(2)
    return type_map


def process_hdf5_fc_interop(tree, options):
    """Mirrors Process_HDF5FCInterop() from HDF5FCInterop.pm."""
    type_map = _load_type_map()

    for node in walk_tree(tree):
        if node.get('type') != 'declaration':
            continue

        type_changed = False
        types_added  = {}
        for decl in node.get('declarations', []):
            if decl.get('intrinsic') != 'integer':
                continue
            type_val = decl.get('type')
            if type_val is None:
                continue
            m = re.match(r'(kind=)?\s*(\S+)', type_val, re.IGNORECASE)
            if not m:
                continue
            prefix = m.group(1) or ''
            kind   = m.group(2).lower()
            if kind not in type_map:
                continue
            decl['type'] = prefix + type_map[kind]
            type_changed = True
            types_added[type_map[kind]] = True

        if not type_changed:
            continue

        build_declarations(node)

        # Find the first sibling moduleUse of the enclosing unit.  If it does
        # not already import ISO_C_Binding, add it with the new symbols.
        parent = node.get('parent')
        if parent is None:
            continue
        sibling = parent.get('firstChild')
        while sibling is not None:
            if sibling.get('type') == 'moduleUse':
                existing = {k.lower() for k in sibling.get('moduleUse', {}).keys()}
                if 'iso_c_binding' not in existing:
                    add_uses(parent, {
                        'moduleUse': {
                            'ISO_C_Binding': {
                                'intrinsic': True,
                                'only':      dict(types_added),
                            },
                        },
                        'moduleOrder': ['ISO_C_Binding'],
                    })
                break
            sibling = sibling.get('sibling')


register_process('hdf5FCInterop', process_hdf5_fc_interop)
