r"""Regression test for HDF5FCInterop's `hdf5FCInterop.dat` reader.

Bug: the file generated by `hdf5FCInterop.exe` has a leading space on
every line (e.g. " herr_t = c_int").  An earlier draft used `re.match`
(anchored at the start of the line), which silently rejected every entry
and left the build emitting bare `integer(kind=herr_t)` declarations the
Fortran compiler couldn't resolve.

The fix uses `re.search` so the regex floats past the leading whitespace.
"""

import textwrap

from Galacticus.Build.SourceTree.Process.HDF5FCInterop import _load_type_map


def test_load_type_map_skips_leading_whitespace(tmp_path, monkeypatch):
    dat = tmp_path / "hdf5FCInterop.dat"
    dat.write_text(textwrap.dedent("""\
        # comment
         herr_t = c_int
         hid_t  = c_long
        hsize_t  =c_size_t
    """))
    monkeypatch.setenv('BUILDPATH', str(tmp_path))

    type_map = _load_type_map()
    assert type_map == {
        'herr_t':  'c_int',
        'hid_t':   'c_long',
        'hsize_t': 'c_size_t',
    }


def test_load_type_map_missing_file_raises(tmp_path, monkeypatch):
    monkeypatch.setenv('BUILDPATH', str(tmp_path))
    import pytest
    with pytest.raises(RuntimeError, match='does not exist'):
        _load_type_map()
